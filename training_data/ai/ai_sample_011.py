9.# Graph with Adjacency List
class Graph:
    def __init__(self):
        self.graph = {}

    def add_edge(self, u, v):
        if u not in self.graph:
            self.graph[u] = []
        self.graph[u].append(v)

    def display(self):
        for node, edges in self.graph.items():
            print(f"{node} -> {edges}")